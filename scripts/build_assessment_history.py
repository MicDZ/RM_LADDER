def main():
    years = sorted(datasets.keys())

    # Collect all school names
    all_schools = set()
    for year, entries in datasets.items():
        for _, school, _ in entries:
            all_schools.add(normalize(school))

    # Build per-school history
    school_history = {}
    for school in sorted(all_schools):
        entries = []
        for year in years:
            year_entries = datasets[year]
            score = None
            raw_rank = None
            for rank, raw_name, s in year_entries:
                if normalize(raw_name) == school:
                    score = s
                    raw_rank = rank
                    break
            if score is not None:
                entries.append({"year": year, "rank": raw_rank, "score": score})
        if entries:
            school_history[school] = entries

    output = {"years": years, "schools": school_history}
    with open(OUTPUT_FILE, "w", encoding="utf-8") as f:
        json.dump(output, f, ensure_ascii=False, indent=2)

    print(f"Written {len(school_history)} schools to {OUTPUT_FILE}")
    print(f"Years: {years}")


if __name__ == "__main__":
    main()
