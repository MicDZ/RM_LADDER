def main():
    years = sorted(datasets.keys())

    # Collect all school names
    all_schools = set()
    for year, entries in datasets.items():
        for school, _ in entries:
            all_schools.add(normalize(school))

    # Build per-school history with rank (sorted by goldcoin descending within each year)
    school_history = {}
    for school in sorted(all_schools):
        entries = []
        for year in years:
            year_entries = datasets[year]
            # Normalize and sort by goldcoin descending to compute rank
            normalized = [(normalize(s), g) for s, g in year_entries]
            normalized.sort(key=lambda x: -x[1])
            for rank, (s, g) in enumerate(normalized, 1):
                if s == school:
                    entries.append({"year": year, "rank": rank, "goldcoin": g})
                    break
        if entries:
            school_history[school] = entries

    output = {"years": years, "schools": school_history}
    with open(OUTPUT_FILE, "w", encoding="utf-8") as f:
        json.dump(output, f, ensure_ascii=False, indent=2)

    print(f"Written {len(school_history)} schools to {OUTPUT_FILE}")
    print(f"Years: {years}")


if __name__ == "__main__":
    main()
